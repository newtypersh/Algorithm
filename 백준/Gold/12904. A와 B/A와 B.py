S = input().strip()         # 1. 첫 번째 입력을 받아 S에 저장 (앞뒤 공백 제거)
T = input().strip()         # 2. 두 번째 입력을 받아 T에 저장 (앞뒤 공백 제거)

while len(T) > len(S):      # 3. T의 길이가 S보다 클 때만 반복
    if T[-1] == 'A':        # 4. T의 마지막 글자가 'A'라면
        T = T[:-1]          # 5. 마지막 글자 'A'를 제거해 새 T로 저장
    elif T[-1] == 'B':      # 6. T의 마지막 글자가 'B'라면
        T = T[:-1][::-1]    # 7. 마지막 글자 'B'를 빼고 남은 T를 뒤집어서(T[::-1]) 저장

print(1 if T == S else 0)